    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         
        String first=txtFirst.getText();
        final String SQL = "Select * From ContactInfo where FirstName=?";
        try {
            Class.forName("com.mysql.jdbc.Driver");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Driver not found...");
        }
        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/AddressBook", "root", "1234");
            PreparedStatement stm = connection.prepareStatement(SQL);
            stm.setObject(1, first);
            ResultSet rst = stm.executeQuery();
            if(rst.next()){
                txtId.setText(rst.getString("ContactId"));
                 txtLast.setText(rst.getString("LastName"));
                txtAddress.setText(rst.getString("Address"));
                txtContact.setText(rst.getString("Contact"));
                txtEmail.setText(rst.getString("EmailAddress"));
            }else{
                JOptionPane.showMessageDialog(this, "Customer not found...");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

