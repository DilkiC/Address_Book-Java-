                String id = txtId.getText();
                String first = txtFirst.getText();
                String last = txtLast.getText();
                String address = txtAddress.getText();
                int contact = Integer.parseInt(txtContact.getText());
                String email = txtEmail.getText();
                
                final String SQL ="Insert into ContactInfo Values(?,?,?,?,?,?)";
                try {
                    Class.forName("com.mysql.jdbc.Driver");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Driver not found...");
             
        }
                try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/AddressBook", "root", "1234");
            PreparedStatement stm = connection.prepareStatement(SQL);
            stm.setObject(1, id);
            stm.setObject(2, first);
            stm.setObject(3, last);
            stm.setObject(4, address);
            stm.setObject(5, contact);
            stm.setObject(6, email);
            
            int res = stm.executeUpdate();
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Added Success");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
      
